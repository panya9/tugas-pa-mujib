public class Bab4Soal1 {
    public static void main(String[] args) {
        int angka1 = 10;
        int angka2 = 3;

        // Penambahan
        int hasilTambah = angka1 + angka2;
        System.out.println("a. Hasil Pertambahan A + B = " + hasilTambah);

        // Pengurangan
        int hasilKurang = angka1 - angka2;
        System.out.println("b. Hasil Pengurangan A - B = " + hasilKurang);

        // Perkalian
        int hasilKali = angka1 * angka2;
        System.out.println("c. Hasil Perkalian A * B = " + hasilKali);

        // Pembagian
        double hasilBagi = (double) angka1 / angka2;
        System.out.println("d. Hasil Pembagian A / B = " + hasilBagi);
}
}
